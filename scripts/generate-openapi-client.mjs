#!/usr/bin/env node
import assert from "node:assert/strict";
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { loadOpenApiSpec, resolveRef } from "./openapi/schema-validator.mjs";

const OUTPUT_PATH = path.resolve(process.cwd(), "packages/sdk/src/index.ts");
const checkOnly = process.argv.includes("--check");

const spec = await loadOpenApiSpec();
const source = generateClient(spec);

if (checkOnly) {
  const existing = await readFile(OUTPUT_PATH, "utf8").catch(() => "");
  assert.equal(existing, source, "generated SDK is out of date; run `bun run openapi:generate`");
  console.log("Generated SDK is up to date.");
} else {
  await writeFile(OUTPUT_PATH, source);
  console.log(`Generated ${path.relative(process.cwd(), OUTPUT_PATH)}`);
}

function generateClient(spec) {
  const operations = collectOperations(spec);
  for (const operationId of [
    "uploadAsset",
    "listAssets",
    "getAsset",
    "deleteAsset",
    "listAssetEvents",
    "streamAssetEvents",
    "getPlaybackBootstrap",
    "recordPlayerTelemetry",
    "getPlaybackAnalytics"
  ]) {
    assert.ok(operations.get(operationId), `missing operationId ${operationId}`);
  }

  const schemaTypes = Object.entries(spec.components.schemas)
    .map(([name, schema]) => `export type ${name} = ${schemaToType(spec, schema)};\n`)
    .join("\n");

  const paths = Object.fromEntries(
    [...operations.entries()].map(([operationId, operation]) => [operationId, operation.path])
  );

  return `/* eslint-disable */
// Generated from docs/openapi/rend-public-api.openapi.json by scripts/generate-openapi-client.mjs.
// Do not edit this file directly. Run \`bun run openapi:generate\` instead.

${schemaTypes}
export type RendClientOptions = {
  apiKey?: string;
  apiBaseUrl?: string;
  siteBaseUrl?: string;
  fetch?: typeof fetch;
  headers?: HeadersInit;
};

export type RendRequestOptions = {
  signal?: AbortSignal;
  headers?: HeadersInit;
};

export type UploadAssetOptions = RendRequestOptions & {
  contentType?: string;
  contentLength?: number;
};

export type ListAssetsOptions = RendRequestOptions & {
  limit?: number;
};

export type ListAssetEventsOptions = RendRequestOptions & {
  afterSequence?: number;
  limit?: number;
};

export type StreamAssetEventsOptions = RendRequestOptions & {
  afterSequence?: number;
  assetId?: string;
};

export type PlaybackBootstrapOptions = RendRequestOptions & {
  playbackBaseUrl?: string;
};

export type PlaybackAnalyticsOptions = RendRequestOptions & {
  windowSeconds?: number;
};

export type WaitForPlayableAssetOptions = RendRequestOptions & {
  intervalMs?: number;
  timeoutMs?: number;
};

type JsonRecord = Record<string, unknown>;
type RequestInitWithDuplex = RequestInit & { duplex?: "half" };

const DEFAULT_API_BASE_URL = "https://api.rend.so";
const DEFAULT_SITE_BASE_URL = "https://rend.so";
const OPERATION_PATHS = ${JSON.stringify(paths, null, 2)} as const;

export class RendApiError extends Error {
  status: number;
  body: unknown;

  constructor(status: number, body: unknown) {
    super(errorMessage(status, body));
    this.name = "RendApiError";
    this.status = status;
    this.body = body;
  }
}

export class RendClient {
  private readonly apiKey?: string;
  private readonly apiBaseUrl: string;
  private readonly siteBaseUrl: string;
  private readonly fetchImpl: typeof fetch;
  private readonly defaultHeaders: HeadersInit | undefined;

  constructor(options: RendClientOptions = {}) {
    this.apiKey = options.apiKey;
    this.apiBaseUrl = normalizeBaseUrl(options.apiBaseUrl ?? DEFAULT_API_BASE_URL);
    this.siteBaseUrl = normalizeBaseUrl(options.siteBaseUrl ?? DEFAULT_SITE_BASE_URL);
    this.fetchImpl = options.fetch ?? globalThis.fetch;
    this.defaultHeaders = options.headers;

    if (!this.fetchImpl) {
      throw new Error("RendClient requires a fetch implementation");
    }
  }

  uploadAsset(body: BodyInit, options: UploadAssetOptions = {}) {
    const headers = new Headers(options.headers);
    headers.set("content-type", options.contentType ?? "application/octet-stream");
    if (options.contentLength !== undefined) {
      headers.set("content-length", String(Math.trunc(options.contentLength)));
    }

    return this.requestJson<AssetUploadResponse>("api", OPERATION_PATHS.uploadAsset, {
      method: "POST",
      body,
      duplex: "half",
      headers,
      signal: options.signal
    });
  }

  listAssets(options: ListAssetsOptions = {}) {
    return this.requestJson<AssetListResponse>(
      "api",
      OPERATION_PATHS.listAssets,
      { method: "GET", headers: options.headers, signal: options.signal },
      { limit: options.limit }
    );
  }

  getAsset(assetId: string, options: RendRequestOptions = {}) {
    return this.requestJson<AssetDetail>("api", assetPath(OPERATION_PATHS.getAsset, assetId), {
      method: "GET",
      headers: options.headers,
      signal: options.signal
    });
  }

  deleteAsset(assetId: string, options: RendRequestOptions = {}) {
    return this.requestJson<AssetDeleteResponse>("api", assetPath(OPERATION_PATHS.deleteAsset, assetId), {
      method: "DELETE",
      headers: options.headers,
      signal: options.signal
    });
  }

  listAssetEvents(assetId: string, options: ListAssetEventsOptions = {}) {
    return this.requestJson<AssetEventsResponse>(
      "api",
      assetPath(OPERATION_PATHS.listAssetEvents, assetId),
      { method: "GET", headers: options.headers, signal: options.signal },
      { after_sequence: options.afterSequence, limit: options.limit }
    );
  }

  async *streamAssetEvents(options: StreamAssetEventsOptions = {}): AsyncGenerator<AssetEvent> {
    const response = await this.requestRaw(
      "api",
      OPERATION_PATHS.streamAssetEvents,
      {
        method: "GET",
        headers: options.headers,
        signal: options.signal
      },
      { after_sequence: options.afterSequence, asset_id: options.assetId }
    );

    if (!response.body) return;

    const decoder = new TextDecoder();
    const reader = response.body.getReader();
    let buffer = "";

    while (true) {
      const chunk = await reader.read();
      if (chunk.done) break;
      buffer += decoder.decode(chunk.value, { stream: true });

      let boundary = buffer.indexOf("\\n\\n");
      while (boundary >= 0) {
        const frame = buffer.slice(0, boundary);
        buffer = buffer.slice(boundary + 2);
        const event = assetEventFromSseFrame(frame);
        if (event) yield event;
        boundary = buffer.indexOf("\\n\\n");
      }
    }

    buffer += decoder.decode();
    const event = assetEventFromSseFrame(buffer);
    if (event) yield event;
  }

  getPlaybackBootstrap(assetId: string, options: PlaybackBootstrapOptions = {}) {
    return this.requestJson<PlaybackBootstrapResponse>(
      "site",
      assetPath(OPERATION_PATHS.getPlaybackBootstrap, assetId),
      { method: "GET", headers: options.headers, signal: options.signal },
      { playbackBaseUrl: options.playbackBaseUrl }
    );
  }

  recordPlayerTelemetry(payload: PlayerTelemetryBatch, options: RendRequestOptions = {}) {
    const headers = new Headers(options.headers);
    headers.set("content-type", "application/json");

    return this.requestJson<PlayerTelemetryAccepted>("site", OPERATION_PATHS.recordPlayerTelemetry, {
      method: "POST",
      body: JSON.stringify(payload),
      headers,
      signal: options.signal
    });
  }

  getPlaybackAnalytics(assetId: string, options: PlaybackAnalyticsOptions = {}) {
    return this.requestJson<PlaybackAnalyticsResponse>(
      "api",
      assetPath(OPERATION_PATHS.getPlaybackAnalytics, assetId),
      { method: "GET", headers: options.headers, signal: options.signal },
      { window_seconds: options.windowSeconds }
    );
  }

  async waitForPlayableAsset(assetId: string, options: WaitForPlayableAssetOptions = {}) {
    const timeoutMs = options.timeoutMs ?? 120_000;
    const intervalMs = options.intervalMs ?? 1_000;
    const started = Date.now();

    while (true) {
      if (options.signal?.aborted) throw new Error("waitForPlayableAsset aborted");
      const asset = await this.getAsset(assetId, options);
      if (asset.playable_state === "opener_ready" || asset.playable_state === "hls_ready") {
        return asset;
      }
      if (asset.playable_state === "failed" || asset.playable_state === "deleted") {
        throw new Error(\`asset \${assetId} reached terminal state \${asset.playable_state}\`);
      }
      if (Date.now() - started >= timeoutMs) {
        throw new Error(\`timed out waiting for asset \${assetId} to become playable\`);
      }
      await sleep(intervalMs);
    }
  }

  private async requestRaw(
    server: "api" | "site",
    requestPath: string,
    init: RequestInitWithDuplex,
    query?: Record<string, string | number | boolean | undefined>
  ) {
    const headers = this.headers(server, init.headers);
    const response = await this.fetchImpl(this.url(server, requestPath, query), {
      ...init,
      headers
    });
    if (!response.ok) {
      throw new RendApiError(response.status, await readResponseBody(response));
    }
    return response;
  }

  private async requestJson<T>(
    server: "api" | "site",
    requestPath: string,
    init: RequestInitWithDuplex,
    query?: Record<string, string | number | boolean | undefined>
  ): Promise<T> {
    const response = await this.requestRaw(server, requestPath, init, query);
    return (await readResponseBody(response)) as T;
  }

  private headers(server: "api" | "site", input?: HeadersInit) {
    const headers = new Headers(this.defaultHeaders);
    for (const [key, value] of new Headers(input)) headers.set(key, value);
    if (server === "api" && this.apiKey && !headers.has("authorization")) {
      headers.set("authorization", \`Bearer \${this.apiKey}\`);
    }
    if (!headers.has("accept")) headers.set("accept", "application/json");
    return headers;
  }

  private url(
    server: "api" | "site",
    requestPath: string,
    query?: Record<string, string | number | boolean | undefined>
  ) {
    const baseUrl = server === "api" ? this.apiBaseUrl : this.siteBaseUrl;
    const url = new URL(requestPath, baseUrl);
    for (const [key, value] of Object.entries(query ?? {})) {
      if (value !== undefined) url.searchParams.set(key, String(value));
    }
    return url;
  }
}

function assetPath(template: string, assetId: string) {
  return template.replace("{assetId}", encodeURIComponent(assetId));
}

function normalizeBaseUrl(value: string) {
  return value.endsWith("/") ? value : \`\${value}/\`;
}

async function readResponseBody(response: Response): Promise<unknown> {
  const text = await response.text();
  if (!text) return undefined;
  const contentType = response.headers.get("content-type") ?? "";
  if (contentType.includes("application/json")) return JSON.parse(text);
  try {
    return JSON.parse(text);
  } catch {
    return text;
  }
}

function errorMessage(status: number, body: unknown) {
  if (isRecord(body)) {
    const message = body.message ?? body.error;
    if (typeof message === "string" && message.trim()) return message;
  }
  return \`Rend API request failed with HTTP \${status}\`;
}

function isRecord(value: unknown): value is JsonRecord {
  return Boolean(value && typeof value === "object" && !Array.isArray(value));
}

function assetEventFromSseFrame(frame: string): AssetEvent | null {
  const data = frame
    .split(/\\r?\\n/)
    .filter((line) => line.startsWith("data:"))
    .map((line) => line.slice("data:".length).trimStart())
    .join("\\n");
  if (!data) return null;
  return JSON.parse(data) as AssetEvent;
}

function sleep(ms: number) {
  return new Promise<void>((resolve) => setTimeout(resolve, ms));
}
`;
}

function collectOperations(spec) {
  const operations = new Map();
  for (const [pathName, pathItem] of Object.entries(spec.paths)) {
    for (const method of ["get", "post", "put", "patch", "delete"]) {
      const operation = pathItem?.[method];
      if (!operation) continue;
      operations.set(operation.operationId, { ...operation, method, path: pathName });
    }
  }
  return operations;
}

function schemaToType(spec, schema) {
  if (!schema || typeof schema !== "object") return "unknown";

  if (schema.$ref) return refTypeName(schema.$ref);
  if (schema.oneOf) return schema.oneOf.map((item) => parenthesize(schemaToType(spec, item))).join(" | ");
  if (schema.anyOf) return schema.anyOf.map((item) => parenthesize(schemaToType(spec, item))).join(" | ");
  if (schema.allOf) return schema.allOf.map((item) => parenthesize(schemaToType(spec, item))).join(" & ");
  if ("const" in schema) return literal(schema.const);
  if (schema.enum) return schema.enum.map(literal).join(" | ");

  const type = Array.isArray(schema.type) ? schema.type[0] : schema.type;
  if (type === "array") return `Array<${schemaToType(spec, schema.items)}>`;
  if (type === "boolean") return "boolean";
  if (type === "integer" || type === "number") return "number";
  if (type === "null") return "null";
  if (type === "string") return "string";
  if (type === "object" || schema.properties || schema.additionalProperties) {
    return objectType(spec, schema);
  }

  const resolved = schema.$ref ? resolveRef(spec, schema.$ref) : null;
  return resolved ? schemaToType(spec, resolved) : "unknown";
}

function objectType(spec, schema) {
  const properties = schema.properties ?? {};
  const entries = Object.entries(properties);
  const required = new Set(schema.required ?? []);

  if (entries.length === 0 && schema.additionalProperties && typeof schema.additionalProperties === "object") {
    return `Record<string, ${schemaToType(spec, schema.additionalProperties)}>`;
  }
  if (entries.length === 0) return "Record<string, unknown>";

  const lines = entries.map(([key, propertySchema]) => {
    const optional = required.has(key) ? "" : "?";
    return `  ${propertyKey(key)}${optional}: ${schemaToType(spec, propertySchema)};`;
  });

  if (schema.additionalProperties === true) {
    lines.push("  [key: string]: unknown;");
  } else if (schema.additionalProperties && typeof schema.additionalProperties === "object") {
    lines.push(`  [key: string]: ${schemaToType(spec, schema.additionalProperties)};`);
  }

  return `{\n${lines.join("\n")}\n}`;
}

function refTypeName(ref) {
  const prefix = "#/components/schemas/";
  assert.ok(ref.startsWith(prefix), `unsupported schema ref ${ref}`);
  return ref.slice(prefix.length);
}

function propertyKey(key) {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(key) ? key : JSON.stringify(key);
}

function parenthesize(type) {
  return type.includes(" | ") || type.includes(" & ") ? `(${type})` : type;
}

function literal(value) {
  return JSON.stringify(value);
}
